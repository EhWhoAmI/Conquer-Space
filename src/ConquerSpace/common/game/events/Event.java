package ConquerSpace.common.game.events;

/**
 *
 * @author EhWhoAmI
 */
public class Event {

    private int id;
    private String text;

    public Event(String text) {
        this.text = text;
    }

    public int getId() {
        return id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return text;
    }
}
