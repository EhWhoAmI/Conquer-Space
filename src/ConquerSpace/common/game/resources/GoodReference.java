package ConquerSpace.common.game.resources;

/**
 *
 * @author EhWhoAmI
 */
public class GoodReference extends StoreableReference {
    public static final GoodReference INVALID_REFERENCE = new GoodReference(-1);
    private final int id;

    public GoodReference(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 13 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final GoodReference other = (GoodReference) obj;
        return this.id != other.id;
    }

    @Override
    public String getName() {
        return "";
    }
}
